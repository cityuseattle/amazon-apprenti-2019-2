# declare list with square brackets []
backpack = ['book', 'laptop', 'pen', 'banana', 2, True]

# append on end of list with .append function
backpack.append('keys')
print(backpack)

# insert item at specified index with .insert(index, value) function
backpack.insert(0, 'charger')
print(backpack)

# remove first occurance of specified value with .remove function
backpack.remove('charger')
print(backpack)